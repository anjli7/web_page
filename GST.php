    <h2 class="section-title custom-heading middle-line">Frequently Asked Questions</h2>
    <div class="faq-container">
        <div class="faq-item">
            <div class="faq-question" onclick="toggleFAQ(this)">
                What is the threshold limit for GST registration?
                <span>+</span>
            </div>
            <div class="faq-answer">
                <p>The threshold limit for GST registration is ₹40 lakh for goods suppliers (₹20 lakh for special category states) and ₹20 lakh for service providers (₹10 lakh for special category states).</p>
            </div>
        </div>
        
        <div class="faq-item">
            <div class="faq-question" onclick="toggleFAQ(this)">
                How often do I need to file GST returns?
                <span>+</span>
            </div>
            <div class="faq-answer">
                <p>Regular taxpayers need to file GSTR-1 monthly or quarterly (based on turnover) and GSTR-3B monthly. Annual return (GSTR-9) is filed once a year.</p>
               
            </div>
        </div>
        
        <div class="faq-item">
            <div class="faq-question" onclick="toggleFAQ(this)">
                What is the penalty for late GST filing?
                <span>+</span>
            </div>
            <div class="faq-answer">
                <p>Late fee is ₹50 per day (₹25 CGST + ₹25 SGST) for each day of delay, up to a maximum of ₹5,000. There's also interest at 18% p.a. on the tax due.</p>
            </div>
        </div>
        
        <div class="faq-item">
            <div class="faq-question" onclick="toggleFAQ(this)">
                Can I claim Input Tax Credit without GST registration?
                <span>+</span>
            </div>
            <div class="faq-answer">
                <p>No, only registered taxpayers can claim Input Tax Credit (ITC) under GST. Unregistered businesses cannot claim ITC on their purchases.</p>
            </div>
        </div>
    </div>
</div>
</section>


